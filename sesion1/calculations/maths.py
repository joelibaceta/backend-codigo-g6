def suma(n1, n2):
  """Esta funcion realiza una suma"""
  return n1 + n2

def resta(n1, n2):
  """Esta funcion realiza una resta"""
  return n1 - n2

def multiplicacion(n1, n2):
  """Esta funcion realiza una multiplicacion"""
  return n1 * n2

def division(n1, n2):
  """Esta funcion realiza una division"""
  return n1 / n2